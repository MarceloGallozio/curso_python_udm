"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""

# Aspas simples
print('Marcelo Gallozio')
print(1, 'Marcelo "Gallozio"')

# Aspas duplas
print("Marcelo Gallozio")
print(2, "Marcelo 'Gallozio")

# Escape
print("Marcelo \"Gallozio\"")

# r
print(r"Marcelo \"Gallozio\"")